function graduates (students) {
	// Code disini
	let studentClassObj = {};
	for(let i = 0; i < students.length; i++){
		let studentName  = students[i]["name"]
		let studentClass = students[i]["class"];
		let studentScore = students[i]["score"];		
	
		if(!studentClassObj[studentClass]){
			studentClassObj[studentClass] = [];
		}

		if(studentScore > 75){
			let lastIndexArr = studentClassObj[studentClass].length;
			studentClassObj[studentClass][lastIndexArr++] = {name: studentName,score: studentScore};
		}
	}
	return studentClassObj;
}

